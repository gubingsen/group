package com.three.memory.domain;

public class Inventory {
    private Integer id;

    private Integer proclaid;

    private Integer num;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getProclaid() {
        return proclaid;
    }

    public void setProclaid(Integer proclaid) {
        this.proclaid = proclaid;
    }

    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }
}